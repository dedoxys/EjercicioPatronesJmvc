package ejercicioPatronesNombre;

public class Persona {
	
	private String nombre;
	private String apellidos;
	private int anyoNacimiento;
	private String ciudad;
	private int telefono;
	
	public String getNombre() {
		return this.nombre;
	}
	public void setNombre(String _nombre) {
		this.nombre = _nombre;
	}
	public String getApellidos() {
		return this.apellidos;
	}
	public void setApellidos(String _apellidos) {
		this.apellidos = _apellidos;
	}
	public int getAnyoNacimiento() {
		return this.anyoNacimiento;
	}
	public void setAnyoNacimiento(int _anyoNacimiento) {
		this.anyoNacimiento = _anyoNacimiento;
	}
	public String getCiudad() {
		return this.ciudad;
	}
	public void setCiudad(String _ciudad) {
		this.ciudad = _ciudad;
	}
	public int getTelefono() {
		return this.telefono;
	}
	public void setTelefono(int _telefono) {
		this.telefono = _telefono;
	}
	
	
	
}
